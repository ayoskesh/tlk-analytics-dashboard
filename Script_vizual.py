import pandas as pd
import sqlite3

# Шаг 1. Подготовка данных: создаем in-memory базу данных SQLite
conn = sqlite3.connect(':memory:')

# Словарь с названиями файлов (убедитесь, что имена файлов совпадают с вашими)
files = {
    'Города': 'Датасет грузоперевозки - Города.csv',
    'Клиенты': 'Датасет грузоперевозки - Клиенты.csv',
    'Водители': 'Датасет грузоперевозки - Водители.csv',
    'Транспорт': 'Датасет грузоперевозки - Транспорт.csv',
    'Маршруты': 'Датасет грузоперевозки - Маршруты.csv',
    'Рейсы': 'Датасет грузоперевозки - Рейсы.csv',
    'Грузы': 'Датасет грузоперевозки - Грузы.csv',
    'Платежи': 'Датасет грузоперевозки - Платежи.csv'
}

# Загружаем CSV-файлы в SQL-таблицы
for table_name, file_path in files.items():
    df = pd.read_csv(file_path)
    df.to_sql(table_name, conn, index=False, if_exists='replace')

print("Данные успешно загружены. Выполняю анализ...\n" + "="*50)

# ==========================================
# Шаг 2. Основной анализ данных
# ==========================================

# Задача 1: Топ-5 клиентов по общей стоимости их грузов
q1 = """
SELECT 
    c.Название_клиента, 
    c.Тип_клиента,
    SUM(g.Стоимость_груза) AS Общая_стоимость_грузов
FROM Клиенты c
JOIN Грузы g ON c.ID_клиента = g.ID_клиента
GROUP BY c.ID_клиента
ORDER BY Общая_стоимость_грузов DESC
LIMIT 5;
"""
print("\n--- Задача 1: Топ-5 клиентов по суммарной стоимости грузов ---")
print(pd.read_sql_query(q1, conn).to_string(index=False))

# Задача 2: Эффективность транспорта (средняя стоимость рейса по типам)
q2 = """
SELECT 
    t.Тип_транспорта, 
    ROUND(AVG(r.Стоимость_рейса), 2) AS Средняя_стоимость_рейса
FROM Транспорт t
JOIN Рейсы r ON t.ID_транспорта = r.ID_транспорта
GROUP BY t.Тип_транспорта
ORDER BY Средняя_стоимость_рейса DESC;
"""
print("\n--- Задача 2: Средняя стоимость рейса по типам транспорта ---")
print(pd.read_sql_query(q2, conn).to_string(index=False))

# Задача 3: Статистика водителей (Топ-3 водителя по количеству ДОСТАВЛЕННЫХ рейсов)
q3 = """
SELECT 
    v.Имя, 
    v.Фамилия, 
    COUNT(r.ID_рейса) AS Успешных_рейсов
FROM Водители v
JOIN Рейсы r ON v.ID_водителя = r.ID_водителя
WHERE r.Статус_рейса = 'Доставлен'
GROUP BY v.ID_водителя
ORDER BY Успешных_рейсов DESC
LIMIT 3;
"""
print("\n--- Задача 3: Топ-3 водителя по количеству успешных доставок ---")
print(pd.read_sql_query(q3, conn).to_string(index=False))

# ==========================================
# Шаг 3. Продвинутый анализ
# ==========================================

# Задача 4: Финансовый анализ маршрутов (самый прибыльный маршрут)
q4 = """
SELECT 
    c1.Название_города AS Откуда, 
    c2.Название_города AS Куда, 
    SUM(r.Стоимость_рейса) AS Суммарная_выручка
FROM Рейсы r
JOIN Маршруты m ON r.ID_маршрута = m.ID_маршрута
JOIN Города c1 ON m.ID_города_отправления = c1.ID_города
JOIN Города c2 ON m.ID_города_назначения = c2.ID_города
GROUP BY m.ID_маршрута
ORDER BY Суммарная_выручка DESC
LIMIT 1;
"""
print("\n--- Задача 4: Самый прибыльный маршрут (Город А -> Город Б) ---")
print(pd.read_sql_query(q4, conn).to_string(index=False))

# Задача 5: Анализ грузов (самый частый тип по количеству и по весу)
q5 = """
SELECT 
    Тип_груза, 
    COUNT(ID_груза) AS Количество_перевозок, 
    ROUND(SUM(Вес_тонн), 2) AS Общий_перевезенный_вес_тонн
FROM Грузы
GROUP BY Тип_груза
ORDER BY Количество_перевозок DESC;
"""
print("\n--- Задача 5: Статистика по типам перевозимых грузов ---")
print(pd.read_sql_query(q5, conn).to_string(index=False))

# Закрываем соединение
conn.close()